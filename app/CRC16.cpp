/*
 * CRC Calculation Algorithm
 * The CRC is contained in the packet header and is calculated for the
 * entire packet (header plus payload). The CRC calculation is performed
 * with the CRC bytes of the packet header initialized to zero.
 *
 * from F1_RS232_rev1.5.pdf
 */
unsigned short asl::CRC16(unsigned char* pcData, unsigned int nCount)
{
    unsigned char cCRCHi = 0xFF; // high byte of CRC initialized
    unsigned char cCRCLo = 0xFF; // low byte of CRC initialized
    unsigned char cIndex;        // will index into CRC lookup table
    while (nCount--) {
        cIndex = cCRCHi ^ *pcData++;
        cCRCHi = cCRCLo ^ cCRCHiArray[cIndex];
        cCRCLo = cCRCLoArray[cIndex];
    }
    return (unsigned short) ((cCRCHi << 8) + cCRCLo);
}
